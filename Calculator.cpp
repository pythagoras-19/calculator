#include "Calculator.h"

[[maybe_unused]] int Calculator::add(int a, int b) {
    return a + b;
}

[[maybe_unused]] double Calculator::add(double a, double b) {
    return a + b;
}

[[maybe_unused]] int Calculator::subtract(int a, int b) {
    return a - b;
}

[[maybe_unused]] double Calculator::subtract(double a, double b) {
    return a - b;
}

[[maybe_unused]] int Calculator::multiply(int a, int b) {
    return a * b;
}

[[maybe_unused]] double Calculator::multiply(double a, double b) {
    return a * b;
}

[[maybe_unused]] int Calculator::divide(int a, int b) {
    return a / b;
}

[[maybe_unused]] double Calculator::divide(double a, double b) {
    return a / b;
}